<h3>Generate the JSON File</h3>
<p>This link generates the JSON file to reflect
	recent changes to the database. This file is not created
	manually.</p>
<p>Please note, the /jsons directory in the plugin folder MUST
	be writeable by the web user (apache in most cases) for this
	to work. If you are not certain, we recommend you check with
	your web host to see if you are able to do this.</p>

